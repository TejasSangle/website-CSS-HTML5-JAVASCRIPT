			Agriculture is the backbone of the Indian Economy"- said Mahatma Gandhi six decades ago. Even today, the situation is still the same, with almost the entire economy being sustained by agriculture, which is the mainstay of the villages. It contributes 16% of the overall GDP and accounts for employment of approximately 52% of the Indian population. Rapid growth in agriculture is essential not only for self-reliance but also to earn valuable foreign exchange.

			Though extension services in India have gone through many changes since the Green Revolution, however, the divergent needs of the farmers could not be fully addressed. Specific requirements of the farmers based on their crops and agro climatic situation also could not be addressed on a large scale. Therefore, during the XII Plan, a National Mission on Agricultural Extension & Technologies was formulated encompassing not only Extension & ICT but also Seeds & Planting Material, Mechanisation and Plant Protection.

			AGRI TECH SMS Portal has been conceptualised to give a quantum leap in coverage of farmers and geographical area in a timely, specific, holistic and need based knowledge dissemination among the farmers by leveraging the power of mobile telephony in such a way that all sectors use this platform to not only reach out to the farmers but also to address their concerns and queries.</div>
